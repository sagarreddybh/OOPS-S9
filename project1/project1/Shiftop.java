package project1;

public class Shiftop {
	 public static void main(String[] args) {
	        int x = 8; // binary: 1000

	        System.out.println("Original: " + x);
	        System.out.println("Left Shift by 1: " + (x << 1));  // 1000
	        System.out.println("Right Shift by 1: " + (x >> 1));; // 0100 = 4
	}

}
